    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
       
        int n = Integer.parseInt(txtNum.getValue().toString());
        int resto = n%2;
        float cubo = (float) Math.pow(n, 3);
        float raizQ = (float) Math.sqrt(n);
        float raizC = (float) Math.cbrt(n);
        int abs = Math.abs(n);
        lblRes.setText(Integer.toString(resto));
        
        lblCubo.setText(Float.toString(cubo));
        
        lblRaizQ.setText(String.format("%.2f",raizQ));
        
        lblRaizC.setText(String.format("%.2f",raizC));
        
        lblAbs.setText(Integer.toString(abs));
        
        panCalc.setVisible(true);
    }//GEN-LAST:event_jButton1ActionPerformed

